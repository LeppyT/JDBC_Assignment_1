    private void Button_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_ExcluirActionPerformed
        // TODO add your handling code here:  
        if (IsLinhaSelecionada()) {
            int e = JOptionPane.showConfirmDialog(this, 
                    "Deseja realmente excluir o registro selecionado?", 
                    "Confirmar exclusão", 
                    JOptionPane.YES_NO_OPTION);

            if (e == JOptionPane.YES_OPTION) {
                String cod = grdPacientes.getValueAt(grdPacientes.getSelectedRow(), 0).toString();
                this.c.SQLExecute("DELETE FROM paciente WHERE CodPaciente = " + cod);
                JOptionPane.showMessageDialog(this, "Registro excluído com sucesso");
                this.setDados();
            }
        }                             
    }//GEN-LAST:event_Button_ExcluirActionPerformed

