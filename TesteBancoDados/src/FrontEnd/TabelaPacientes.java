    private void setDados() {
        DefaultTableModel d = (DefaultTableModel) grdPacientes.getModel();
        
        //Deletar linhas
        while(d.getRowCount() > 0) {
            d.removeRow(0);
        }          
        
        try {
            this.c.setResultSet("SELECT * FROM paciente");
            if (this.c.getResultSet().first()) {
                do {
                    Object[] Linha = {
                        this.c.getResultSet().getString("CodPaciente"),
                        this.c.getResultSet().getString("Nome"),
                        this.c.getResultSet().getString("Cpf"),
                        this.c.getResultSet().getString("Altura"),
                        this.c.getResultSet().getString("Peso"),
                    };
                    d.addRow(Linha);
                } while(this.c.getResultSet().next());
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private boolean IsLinhaSelecionada() {
        if (grdPacientes.getRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "O cadastro não possui registros");
            return false;
        } 
        else if (grdPacientes.getSelectedRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "Selecionar um aluno");
            return false;
        }
        else
            return true;
    }
    
    
